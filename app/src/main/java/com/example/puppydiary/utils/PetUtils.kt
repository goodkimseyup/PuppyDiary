// 강아지인지 고양이인지 확인
fun isDog(breed: String): Boolean {
    return dogBreedList.contains(breed)
}

fun isCat(breed: String): Boolean {
    return catBreedList.contains(breed)
}

// 펫 타입 반환
fun getPetType(breed: String): String {
    return when {
        isDog(breed) -> "강아지"
        isCat(breed) -> "고양이"
        else -> "반려동물"
    }
}
